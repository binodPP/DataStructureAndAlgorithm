package com.binod.basicdatastructureandalgorithmoperation.corejavaproblem.overriding;

public class Emp {
    int id;
    String str;

    public Emp(int id, String str) {
        this.id = id;
        this.str = str;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }
}
